class Widening 
{
	public static void main(String[] args) 
	{
		byte a =20;
		System.out.println(a);
		
		short s =  'a';
		System.out.println(s);

		char ch = 'a';
		System.out.println(ch);

		int i = 'a';
		System.out.println(i);

		long l ='a';
		System.out.println(l);
		
		float f ='a';
		System.out.println(f);

		double d = 'a';
		System.out.println(d);
	}
}
